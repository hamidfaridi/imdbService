/// <summary>
/// Movie Class
/// </summary>
public class Movie
{
    /// <summary>
    /// Name of Title
    /// </summary>
    public string Name;

    /// <summary>
    /// Title type like Movie, TV Serie or ...
    /// </summary>
    public string TitleType;

    /// <summary>
    /// Full url for title
    /// </summary>
    public string Url;

    /// <summary>
    /// Genre
    /// </summary>
    public string Genre;

    /// <summary>
    /// Writer(s)
    /// </summary>
    public string Writer;

    /// <summary>
    /// Small description for title
    /// </summary>
    public string Description;

    /// <summary>
    /// Director(s)
    /// </summary>
    public string Director;

    /// <summary>
    /// Count of users have given rating for the title
    /// </summary>
    public string RatingCount;

    /// <summary>
    /// The best rating title has given by users
    /// </summary>
    public string BestRating;

    /// <summary>
    /// The worst rating title has given by users
    /// </summary>
    public string WorstRating;

    /// <summary>
    /// Average rating value
    /// </summary>
    public string RatingValue;
    //////////////

    /// <summary>
    /// Rate
    /// </summary>
    public string Rate;
    
    /// <summary>
    /// Keywords
    /// </summary>
    public string Keywords;
    
    /// <summary>
    /// Publish date for title
    /// </summary>
    public string DatePublished;
    
    /// <summary>
    /// Standard Duration unformatted
    /// </summary>
    public string Duration;
    
    /// <summary>
    /// StandardDuration in Minutes
    /// </summary>
    public string DurationDesc;
    
    /// <summary>
    /// Other durations possibly exist for title
    /// </summary>
    public string Durations;
    
    /// <summary>
    /// Actor(s)
    /// </summary>
    public string Actor;
    
    /// <summary>
    /// Poster data in data Base64 format
    /// </summary>
    public string PosterData;
    
    /// <summary>
    /// Title poster full url
    /// </summary>
    public string PosterUrl;

    /// <summary>
    /// Title thumbnail full url
    /// </summary>
    public string PosterThumbnail;
}